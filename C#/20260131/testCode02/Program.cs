class Program 
{
    static void Main(string[] args)
    {     //二項演算子
        System.Console.WriteLine(10 + 5);
        System.Console.WriteLine(10 - 5);
        System.Console.WriteLine(10 * 5);
        System.Console.WriteLine(10 / 5);
        System.Console.WriteLine(10 % 5);

        System.Console.WriteLine(1 + 2 * 3); //数学の優先順位と同じ順番で計算
        System.Console.WriteLine((1 + 2) * 3); //()をつけるとそちらを優先

    }

}